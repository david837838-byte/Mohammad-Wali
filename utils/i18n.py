def get_current_language():
    if has_request_context():
        lang = session.get("lang", "ar")
        if lang in LANGUAGES:
            return lang
    return "ar"


def is_rtl():
    return LANGUAGES[get_current_language()]["dir"] == "rtl"


def t(text, **kwargs):
    lang = get_current_language()
    if lang == "ar":
        template = TRANSLATIONS_AR.get(text, text)
    else:
        template = TRANSLATIONS_EN.get(text, text)
    return template.format(**kwargs)


