def format_localized_date(value=None):
    current = value or datetime.now()
    if isinstance(current, str):
        try:
            current = datetime.fromisoformat(current.replace("Z", ""))
        except ValueError:
            return current
    if isinstance(current, date) and not isinstance(current, datetime):
        current = datetime.combine(current, datetime.min.time())

    ar_days = ["الاثنين", "الثلاثاء", "الأربعاء", "الخميس", "الجمعة", "السبت", "الأحد"]
    ar_months = [
        "يناير", "فبراير", "مارس", "أبريل", "مايو", "يونيو",
        "يوليو", "أغسطس", "سبتمبر", "أكتوبر", "نوفمبر", "ديسمبر"
    ]
    en_days = ["Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday", "Sunday"]
    en_months = [
        "January", "February", "March", "April", "May", "June",
        "July", "August", "September", "October", "November", "December"
    ]

    if get_current_language() == "ar":
        return f"{ar_days[current.weekday()]} {current.day} {ar_months[current.month - 1]} {current.year}"
    return f"{en_days[current.weekday()]}, {en_months[current.month - 1]} {current.day}, {current.year}"

